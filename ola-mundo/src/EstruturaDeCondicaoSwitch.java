public class EstruturaDeCondicaoSwitch {

    public static void main(String[] args) {

        int numero3 = 18;

        switch (numero3) {
            case 18 :
                System.out.println("O número é 18");
                break;
            case 17 :
                System.out.println("O número é 17");
                break;
            default:
                System.out.println("O número é desconhecido");
               break;
        }
    }
}
